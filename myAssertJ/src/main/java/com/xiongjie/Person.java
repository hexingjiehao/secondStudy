package com.xiongjie;

public class Person {

    String name;
    String hobby;
    String address;
    String email;

    public Person() {
    }

    public Person(String name, String hobby, String address, String email) {
        this.name = name;
        this.hobby = hobby;
        this.address = address;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHobby() {
        return hobby;
    }

    public void setHobby(String hobby) {
        this.hobby = hobby;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
